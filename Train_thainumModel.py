y_true_bin = label_binarize(y_true, classes=[0, 1, 2, 3, 4])
n_classes = len(CLASSES)

# 2. คำนวณค่า FPR, TPR และ AUC ของแต่ละคลาส
fpr = dict()
tpr = dict()
roc_auc = dict()

for i in range(n_classes):
    # Y_pred คือค่า Probability ที่ได้จาก model.predict() ในขั้นตอนก่อนหน้า
    fpr[i], tpr[i], _ = roc_curve(y_true_bin[:, i], Y_pred[:, i])
    roc_auc[i] = auc(fpr[i], tpr[i])

# 3. คำนวณ Micro-average ROC curve และ AUC (ภาพรวมทั้งโมเดล)
fpr["micro"], tpr["micro"], _ = roc_curve(y_true_bin.ravel(), Y_pred.ravel())
roc_auc["micro"] = auc(fpr["micro"], tpr["micro"])

# --- 9. วาดกราฟ ROC Curve ---

plt.figure(figsize=(10, 8))

# วาดเส้น Micro-average (เส้นประหนา)
plt.plot(fpr["micro"], tpr["micro"],
         label=f'Micro-average ROC (area = {roc_auc["micro"]:0.2f})',
         color='deeppink', linestyle=':', linewidth=4)

# วาดเส้นของแต่ละคลาส (56, 57, 58, 59, 60)
colors = cycle(['aqua', 'darkorange', 'cornflowerblue', 'red', 'green'])
for i, color in zip(range(n_classes), colors):
    plt.plot(fpr[i], tpr[i], color=color, lw=2,
             label=f'ROC of class {CLASSES[i]} (AUC = {roc_auc[i]:0.2f})')

# วาดเส้น Baseline (เดาสุ่ม)
plt.plot([0, 1], [0, 1], 'k--', lw=2)

plt.xlim([0.0, 1.0])
plt.ylim([0.0, 1.05])
plt.xlabel('False Positive Rate ')
plt.ylabel('True Positive Rate ')
plt.title('ROC Curve & AUC Score: Thai Numbers Identification', fontsize=16)
plt.legend(loc="lower right")
plt.grid(alpha=0.3)
plt.show()

# พิมพ์ค่า AUC สรุปอีกครั้ง
print(f"\n[SUMMARY] Overall AUC Score (Micro): {roc_auc['micro']:.4f}")
for i in range(n_classes):
    print(f"Class {CLASSES[i]} AUC: {roc_auc[i]:.4f}")

