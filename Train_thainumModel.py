def create_final_image(text, save_path):
    # เริ่มต้นด้วย Canvas ขนาดใหญ่เพื่อให้บิดภาพได้เนียน (Supersampling)
    canvas_size = 64
    img = Image.new('L', (canvas_size, canvas_size), color=0)
    draw = ImageDraw.Draw(img)

    # เลือก Font สุ่ม
    f_path = random.choice(THAI_FONTS)

    # <<< CONFIG: ขนาดตัวอักษร (สัมพันธ์กับ canvas_size) >>>
    font_size = random.randint(26, 34)
    font = ImageFont.truetype(f_path, font_size)

    # คำนวณตำแหน่งกึ่งกลาง
    left, top, right, bottom = draw.textbbox((0, 0), text, font=font)
    w, h = right - left, bottom - top

    # <<< CONFIG: การเขย่าตำแหน่ง (Jitter) >>>
    # ปรับเลขใน randint เพื่อให้ตัวเลขไม่อยู่ตรงกลางเป๊ะ
    x = (canvas_size - w) // 2 + random.randint(-6, 6)
    y = (canvas_size - h) // 2 + random.randint(-6, 6)

    draw.text((x, y), text, fill=255, font=font)

    # --- ขั้นตอน Augmentation (ทำให้ยากขึ้น) ---

    # 1. <<< CONFIG: การหมุน (Rotation) >>>
    # ปรับองศาได้ (-45, 45 จะยากมาก)
    img = img.rotate(random.randint(-25, 25), resample=Image.BICUBIC)

    # 2. <<< CONFIG: การบิดภาพ (Affine/Perspective) >>>
    # ทำให้ภาพดูเหมือนถ่ายจากมุมเอียง
    if random.random() > 0.5:
        m = random.uniform(-0.2, 0.2) # แรงบิด
        img = img.transform((canvas_size, canvas_size), Image.AFFINE,
                            (1, m, 0, 0, 1, 0), Image.BICUBIC)

    # 3. <<< CONFIG: ความเบลอและความคม (Blur/Sharpness) >>>
    if random.random() > 0.7:
        # สุ่มเบลอ (เหมือนภาพหลุดโฟกัส)
        img = img.filter(ImageFilter.GaussianBlur(radius=random.uniform(0.5, 1.0)))
    elif random.random() > 0.7:
        # สุ่มเร่งความคม (ทำให้เส้นแตก)
        enhancer = ImageEnhance.Sharpness(img)
        img = enhancer.enhance(random.uniform(2.0, 4.0))

    # 4. ย่อขนาดลงเหลือ 28x28 สำหรับ Model
    img = img.resize((28, 28), Image.LANCZOS)

    # 5. <<< CONFIG: สัญญาณรบกวน (Noise) >>>
    img_array = np.array(img).astype(float)

    # Gaussian Noise (ความสว่างไม่คงที่)
    noise_strength = random.uniform(5, 15) # ยิ่งเลขเยอะ ยิ่งมีเม็ดทรายเยอะ
    img_array += np.random.normal(0, noise_strength, img_array.shape)

    # Salt & Pepper Noise (จุดขาวดำ)
    if random.random() > 0.8:
        prob = 0.02 # โอกาสเกิดจุด
        for i in range(img_array.shape[0]):
            for j in range(img_array.shape[1]):
                rdn = random.random()
                if rdn < prob: img_array[i][j] = 0
                elif rdn > (1-prob): img_array[i][j] = 255

    # ตัดส่วนเกินและบันทึก
    img_array = np.clip(img_array, 0, 255).astype('uint8')
    Image.fromarray(img_array).save(save_path)

